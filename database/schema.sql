-- AUTO REPUBLIC MANAGER MySQL Schema

CREATE DATABASE IF NOT EXISTS auto_republic DEFAULT CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;
USE auto_republic;

